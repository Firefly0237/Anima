"""Offline CharacterEval/BaichuanCharRM JSONL scorer.

This harness is intentionally eval-only. It consumes policy rollouts that were
already generated by a separate policy job, then scores them with
``morecry/BaichuanCharRM`` in the isolated ``anima-rm-eval`` environment.

Expected input JSONL fields, with accepted aliases:
- ``id``: optional stable example id.
- ``profile`` / ``character_profile`` / ``persona``: character profile text.
- ``conversations`` / ``messages`` / ``dialogue`` / ``history`` / ``prompt``:
  dialogue context, either a string or a list of role/content messages.
- ``response`` / ``candidate_response`` / ``completion`` / ``answer``:
  the candidate model response to score.

The output JSONL preserves one row per input and adds ``charactereval`` with the
score or a fail-fast error. Use ``--dry-run`` to validate JSONL shape and output
a scaffold without loading the 13B model.
"""

from __future__ import annotations

import argparse
import contextlib
import importlib.metadata
import json
import sys
from dataclasses import dataclass
from pathlib import Path
from typing import Any, Iterable, Iterator


MODEL_NAME = "morecry/BaichuanCharRM"
REQUIRED_TRANSFORMERS = "4.30.2"

ENV_HELP = f"""
BaichuanCharRM must run in the isolated anima-rm-eval environment:
  - transformers=={REQUIRED_TRANSFORMERS}
  - trust_remote_code enabled explicitly
  - bitsandbytes 4-bit NF4 quantization for the 13B model
  - offline scoring only; do not co-load the policy model

Typical run:
  python -m anima.eval.run_charactereval \\
    --input-jsonl work/eval/rollouts.jsonl \\
    --output-jsonl work/eval/charrm_scores.jsonl \\
    --trust-remote-code
""".strip()


@dataclass(frozen=True)
class EvalExample:
    """Normalized fields needed by the RM."""

    line_no: int
    record: dict[str, Any]
    example_id: str
    profile: str
    dialogue: str
    response: str


def _version_of(package: str) -> str | None:
    try:
        return importlib.metadata.version(package)
    except importlib.metadata.PackageNotFoundError:
        return None


def fail_fast_env(args: argparse.Namespace) -> None:
    """Check the brittle RM environment before touching model weights."""

    transformers_version = _version_of("transformers")
    if transformers_version != REQUIRED_TRANSFORMERS:
        raise SystemExit(
            "BaichuanCharRM requires transformers=="
            f"{REQUIRED_TRANSFORMERS}; found {transformers_version or 'not installed'}.\n"
            f"{ENV_HELP}"
        )

    if not args.trust_remote_code:
        raise SystemExit(
            "Refusing to load BaichuanCharRM without --trust-remote-code.\n"
            f"{ENV_HELP}"
        )

    if args.load_in_4bit and _version_of("bitsandbytes") is None:
        raise SystemExit(
            "4-bit scoring needs bitsandbytes installed in anima-rm-eval.\n"
            f"{ENV_HELP}"
        )


def load_jsonl(path: Path) -> Iterator[tuple[int, dict[str, Any]]]:
    with path.open("r", encoding="utf-8") as handle:
        for line_no, line in enumerate(handle, start=1):
            line = line.strip()
            if not line:
                continue
            try:
                record = json.loads(line)
            except json.JSONDecodeError as exc:
                raise ValueError(f"{path}:{line_no}: invalid JSON: {exc}") from exc
            if not isinstance(record, dict):
                raise ValueError(f"{path}:{line_no}: each JSONL row must be an object")
            yield line_no, record


def dump_jsonl(path: Path, rows: Iterable[dict[str, Any]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as handle:
        for row in rows:
            handle.write(json.dumps(row, ensure_ascii=False, sort_keys=True))
            handle.write("\n")


def _first_present(record: dict[str, Any], names: tuple[str, ...]) -> Any:
    for name in names:
        value = record.get(name)
        if value not in (None, ""):
            return value
    return None


def _render_dialogue(value: Any) -> str:
    if isinstance(value, str):
        return value.strip()
    if isinstance(value, list):
        parts: list[str] = []
        for turn in value:
            if isinstance(turn, dict):
                role = str(turn.get("role") or turn.get("speaker") or "unknown").strip()
                content = str(turn.get("content") or turn.get("text") or "").strip()
                if content:
                    parts.append(f"{role}: {content}")
            elif turn is not None:
                text = str(turn).strip()
                if text:
                    parts.append(text)
        return "\n".join(parts)
    return str(value).strip() if value is not None else ""


def normalize_record(line_no: int, record: dict[str, Any]) -> EvalExample:
    profile = _first_present(record, ("profile", "character_profile", "persona"))
    dialogue = _first_present(
        record,
        ("conversations", "messages", "dialogue", "history", "prompt"),
    )
    response = _first_present(
        record,
        ("response", "candidate_response", "candidate", "completion", "answer", "model_response"),
    )

    rendered_profile = str(profile).strip() if profile is not None else ""
    rendered_dialogue = _render_dialogue(dialogue)
    rendered_response = str(response).strip() if response is not None else ""

    missing = []
    if not rendered_profile:
        missing.append("profile")
    if not rendered_dialogue:
        missing.append("conversations/dialogue/prompt")
    if not rendered_response:
        missing.append("response")
    if missing:
        raise ValueError(f"line {line_no}: missing required field(s): {', '.join(missing)}")

    example_id = str(record.get("id") or f"line_{line_no}")
    return EvalExample(
        line_no=line_no,
        record=record,
        example_id=example_id,
        profile=rendered_profile,
        dialogue=rendered_dialogue,
        response=rendered_response,
    )


def build_rm_text(example: EvalExample) -> str:
    """Render a stable RM input string from profile, dialogue, and response."""

    return (
        "角色档案:\n"
        f"{example.profile}\n\n"
        "对话上下文:\n"
        f"{example.dialogue}\n\n"
        "候选回复:\n"
        f"{example.response}"
    )


def batched(items: list[EvalExample], batch_size: int) -> Iterator[list[EvalExample]]:
    for start in range(0, len(items), batch_size):
        yield items[start : start + batch_size]


def _dynamic_rm_class(model_name: str, class_ref: str, local_files_only: bool, revision: str | None):
    """Load the remote BaichuanCharRM class across old/new transformers APIs."""

    from transformers.dynamic_module_utils import get_class_from_dynamic_module

    try:
        return get_class_from_dynamic_module(
            class_ref,
            model_name,
            local_files_only=local_files_only,
            revision=revision,
        )
    except TypeError:
        module_file, class_name = class_ref.rsplit(".", 1)
        if not module_file.endswith(".py"):
            module_file = f"{module_file}.py"
        return get_class_from_dynamic_module(
            model_name,
            module_file,
            class_name,
            local_files_only=local_files_only,
            revision=revision,
        )


@contextlib.contextmanager
def _skip_to_for_quantized_models():
    """Neutralize ``model.to(device)`` for already-quantized models during load.

    On the pinned RM env (transformers 4.30.2 + an old accelerate), loading a
    4/8-bit model that fits on one device makes ``accelerate.dispatch_model``
    call ``model.to(device)``. Transformers forbids ``.to`` on quantized weights
    because they are already placed and cast during quantized loading, so the
    load aborts with "ValueError: .to is not supported for 4-bit ...".
    Newer accelerate guards this case; the pinned versions do not. We make
    ``PreTrainedModel.to`` a no-op only for already-quantized models, only for
    the duration of ``from_pretrained``. This mirrors the upstream accelerate fix
    and changes nothing for unquantized models.
    """

    import transformers.modeling_utils as modeling_utils

    original_to = modeling_utils.PreTrainedModel.to

    def patched_to(self, *args: Any, **kwargs: Any):
        if (
            getattr(self, "is_quantized", False)
            or getattr(self, "is_loaded_in_4bit", False)
            or getattr(self, "is_loaded_in_8bit", False)
        ):
            return self
        return original_to(self, *args, **kwargs)

    modeling_utils.PreTrainedModel.to = patched_to
    try:
        yield
    finally:
        modeling_utils.PreTrainedModel.to = original_to


def load_rm(args: argparse.Namespace):
    """Load tokenizer + BaichuanCharRM in 4-bit if available."""

    import torch
    from transformers import AutoModel, AutoTokenizer, BitsAndBytesConfig

    quantization_config = None
    if args.load_in_4bit:
        quantization_config = BitsAndBytesConfig(
            load_in_4bit=True,
            bnb_4bit_quant_type="nf4",
            bnb_4bit_use_double_quant=True,
            bnb_4bit_compute_dtype=torch.bfloat16,
        )

    load_kwargs = {
        "trust_remote_code": True,
        "local_files_only": args.local_files_only,
        "revision": args.revision,
    }
    model_kwargs = {
        **load_kwargs,
        "device_map": "auto",
        "torch_dtype": "auto",
    }
    if quantization_config is not None:
        model_kwargs["quantization_config"] = quantization_config

    tokenizer = AutoTokenizer.from_pretrained(args.model_name, **load_kwargs)
    load_guard = (
        _skip_to_for_quantized_models()
        if quantization_config is not None
        else contextlib.nullcontext()
    )
    with load_guard:
        try:
            model = AutoModel.from_pretrained(args.model_name, **model_kwargs)
        except ValueError:
            # W1 probe found that this repo may expose BaichuanCharRM without a
            # standard AutoModel config mapping, so fall back to the remote class.
            model_cls = _dynamic_rm_class(
                args.model_name,
                args.model_class,
                args.local_files_only,
                args.revision,
            )
            model = model_cls.from_pretrained(args.model_name, **model_kwargs)

    model.eval()
    return tokenizer, model


def extract_scores(outputs: Any, batch_len: int) -> list[float]:
    """Pull a per-row scalar score from common HF output shapes."""

    raw = None
    if isinstance(outputs, dict):
        for key in ("scores", "score", "logits"):
            if key in outputs:
                raw = outputs[key]
                break
    elif isinstance(outputs, (tuple, list)):
        raw = outputs[1] if len(outputs) > 1 else outputs[0]
    else:
        raw = getattr(outputs, "logits", None)
        if raw is None:
            raw = getattr(outputs, "scores", None)

    if raw is None:
        raise RuntimeError("model output did not contain logits/scores")

    if hasattr(raw, "detach"):
        raw = raw.detach().float().cpu()
        if raw.ndim == 0:
            values = [float(raw.item())]
        elif raw.ndim == 1:
            values = [float(x) for x in raw.tolist()]
        else:
            values = [float(x) for x in raw.reshape(raw.shape[0], -1).mean(dim=1).tolist()]
    elif isinstance(raw, (int, float)):
        values = [float(raw)]
    elif isinstance(raw, list):
        values = [float(x[0] if isinstance(x, list) else x) for x in raw]
    else:
        raise RuntimeError(f"unsupported score type: {type(raw)!r}")

    if len(values) == 1 and batch_len > 1:
        values = values * batch_len
    if len(values) != batch_len:
        raise RuntimeError(f"expected {batch_len} scores, got {len(values)}")
    return values


def score_batch(
    examples: list[EvalExample],
    tokenizer: Any,
    model: Any,
    max_length: int,
) -> list[float]:
    import torch

    texts = [build_rm_text(example) for example in examples]
    encoded = tokenizer(
        texts,
        return_tensors="pt",
        padding=True,
        truncation=True,
        max_length=max_length,
    )
    device = getattr(model, "device", None)
    if device is not None:
        encoded = {key: value.to(device) for key, value in encoded.items()}

    with torch.inference_mode():
        outputs = model(**encoded)
    return extract_scores(outputs, len(examples))


def make_output_row(
    example: EvalExample,
    *,
    score: float | None,
    args: argparse.Namespace,
    status: str,
    error: str | None = None,
) -> dict[str, Any]:
    row = dict(example.record)
    row["charactereval"] = {
        "id": example.example_id,
        "model": args.model_name,
        "score": score,
        "status": status,
        "quantization": "4bit-nf4" if args.load_in_4bit else "unquantized",
        "local_files_only": args.local_files_only,
    }
    if error is not None:
        row["charactereval"]["error"] = error
    return row


def parse_args(argv: list[str] | None = None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Offline BaichuanCharRM JSONL scorer for CharacterEval rollouts.",
        epilog=ENV_HELP,
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    parser.add_argument("--input-jsonl", type=Path, required=True)
    parser.add_argument("--output-jsonl", type=Path, required=True)
    parser.add_argument("--model-name", default=MODEL_NAME)
    parser.add_argument("--model-class", default="modeling_baichuan.BaichuanCharRM")
    parser.add_argument("--revision", default=None)
    parser.add_argument("--batch-size", type=int, default=4)
    parser.add_argument("--max-length", type=int, default=2048)
    parser.add_argument("--trust-remote-code", action="store_true")
    parser.add_argument("--dry-run", action="store_true", help="validate JSONL and write score=None rows")
    parser.add_argument(
        "--allow-download",
        action="store_true",
        help="allow HF downloads; default is offline local_files_only=True",
    )
    parser.add_argument(
        "--no-4bit",
        dest="load_in_4bit",
        action="store_false",
        help="disable the default 4-bit NF4 load; intended only for debugging",
    )
    parser.set_defaults(load_in_4bit=True)
    args = parser.parse_args(argv)
    args.local_files_only = not args.allow_download
    if args.batch_size < 1:
        parser.error("--batch-size must be >= 1")
    if args.max_length < 1:
        parser.error("--max-length must be >= 1")
    return args


def run(args: argparse.Namespace) -> int:
    examples = [normalize_record(line_no, row) for line_no, row in load_jsonl(args.input_jsonl)]
    if args.dry_run:
        rows = [
            make_output_row(example, score=None, args=args, status="dry_run")
            for example in examples
        ]
        dump_jsonl(args.output_jsonl, rows)
        print(f"validated {len(examples)} rows; wrote dry-run scores to {args.output_jsonl}")
        return 0

    fail_fast_env(args)
    tokenizer, model = load_rm(args)

    rows: list[dict[str, Any]] = []
    for batch in batched(examples, args.batch_size):
        try:
            scores = score_batch(batch, tokenizer, model, args.max_length)
            for example, score in zip(batch, scores, strict=True):
                rows.append(make_output_row(example, score=score, args=args, status="ok"))
        except Exception as exc:
            for example in batch:
                rows.append(
                    make_output_row(
                        example,
                        score=None,
                        args=args,
                        status="error",
                        error=str(exc),
                    )
                )
            raise

    dump_jsonl(args.output_jsonl, rows)
    print(f"scored {len(rows)} rows with {args.model_name}; wrote {args.output_jsonl}")
    return 0


def main(argv: list[str] | None = None) -> int:
    args = parse_args(argv)
    try:
        return run(args)
    except (OSError, ValueError) as exc:
        print(f"error: {exc}", file=sys.stderr)
        return 2


if __name__ == "__main__":
    raise SystemExit(main())
